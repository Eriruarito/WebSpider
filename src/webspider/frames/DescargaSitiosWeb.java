    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String nombreWeb = Logic.getName(jTextField1.getText());
        String nombreSitios = ("C:\\sitios_web\\sitios.web.data");
        List<String> carpeta = Arrays.asList(new String[]{"Css", "Enlaces", "Html", "Imagenes", "Js", "Multimedia"});
        for(String car : carpeta){
            File directorio = new File("C:\\sitios_web\\" + nombreWeb + "\\" + car);
            if(!directorio.exists()){
            directorio.mkdirs();
            }
        }
        List<String> sitiosWeb = Logic.leerArhivoTextoLista(nombreSitios);
        sitiosWeb.add(nombreWeb);
        Logic.guardarLista(sitiosWeb, nombreSitios);
        if(jCheckBox1.isSelected() && !enlacesW.isEmpty()){
            Logic.guardarLista(enlacesW, ("C:\\sitios_web\\" + nombreWeb + "\\Enlaces\\" + nombreWeb + ".html.data"));
            for(String enlace : enlacesW){
            Logic.getWebArchivo(enlace,("C:\\sitios_web\\" + nombreWeb + "\\Html\\" + enlace.substring(enlace.lastIndexOf("/"))));
            }
        }
        if(jCheckBox2.isSelected() && !enlacesImg.isEmpty()){
            Logic.guardarLista(enlacesImg, ("C:\\sitios_web\\" + nombreWeb + "\\Enlaces\\" + nombreWeb + ".img.data"));
            for(String enlace : enlacesImg){
            Logic.getWebArchivo(enlace,("C:\\sitios_web\\" + nombreWeb + "\\Imagenes\\" + enlace.substring(enlace.lastIndexOf("/"))));
            }
        }
        if(jCheckBox3.isSelected() && !enlacesCss.isEmpty()){
            Logic.guardarLista(enlacesCss, ("C:\\sitios_web\\" + nombreWeb + "\\Enlaces\\" + nombreWeb + ".css.data"));
            for(String enlace : enlacesCss){
            Logic.getWebArchivo(enlace,("C:\\sitios_web\\" + nombreWeb + "\\Css\\" + enlace.substring(enlace.lastIndexOf("/"))));
            }
        }
        if(jCheckBox4.isSelected() && !enlacesJs.isEmpty()){
            Logic.guardarLista(enlacesJs, ("C:\\sitios_web\\" + nombreWeb + "\\Enlaces\\" + nombreWeb + ".javascript.data"));
            for(String enlace : enlacesJs){
            Logic.getWebArchivo(enlace,("C:\\sitios_web\\" + nombreWeb + "\\JavaScript\\" + enlace.substring(enlace.lastIndexOf("/"))));
            }
        }
        if(jCheckBox5.isSelected() && !enlacesMulti.isEmpty()){
            Logic.guardarLista(enlacesMulti, ("C:\\sitios_web\\" + nombreWeb + "\\Enlaces\\" + nombreWeb + ".multimedia.data"));
            for(String enlace : enlacesMulti){
            Logic.getWebArchivo(enlace,("C:\\sitios_web\\" + nombreWeb + "\\Multimedia\\" + enlace.substring(enlace.lastIndexOf("/"))));
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

