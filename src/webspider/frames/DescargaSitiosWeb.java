    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String html = Logic.getHtml(jTextField1.getText());
        if (html.length() > 10){
            jEtiquetaEstadoEnlace.setText("El sitio web esta activo y con conexión");
            jPanel1.setVisible(true);
        }else{
            jEtiquetaEstadoEnlace.setText("El sitio web esta inactivo o sin conexión");
        }
        jEtiquetaEstadoEnlace.setVisible(true);
        enlacesW = Logic.getEnlacesWeb(html, "<a", "href='");
        enlacesImg = Logic.getEnlacesImg(html, "<img", "src='");
        enlacesJs = Logic.getEnlacesJs(html, "<script", "src='");
        enlacesCss = Logic.getEnlacesCss(html, "<link", "href='");
        enlacesMulti = Logic.getEnlacesMulti(html, "<video", "src='");
        enlacesMulti.addAll(Logic.getEnlacesMulti(html, "<audio", "src='"));
        enlacesMulti.addAll(Logic.getEnlacesMulti(html, "<source", "src='"));
        jTextField7.setText(String.valueOf(enlacesW.size()));
        jTextField8.setText(String.valueOf(enlacesImg.size()));
        jTextField9.setText(String.valueOf(enlacesJs.size()));
        jTextField10.setText(String.valueOf(enlacesCss.size()));
        jTextField11.setText(String.valueOf(enlacesMulti.size()));
    }//GEN-LAST:event_jButton1ActionPerformed

